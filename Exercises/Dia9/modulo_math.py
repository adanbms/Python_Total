import math

resultado = math.floor(89.665) #Redodndea el número hacia abajo
print(resultado) #89
resultado = math.ceil(89.665) #Redodndea el número hacia arriba
print(resultado) #90

resultado = math.pi #Valor PI para calculos matematicos
print(resultado) #3.141592653589793

#Logaritmos (A que número hay que exponenciar una base para obtener un resultado)
#log(Resultado, Base)
resultado = math.log(100, 5)
print(resultado) #2.8613531161467867

#Tangente, coseno, etc
resultado = math.tan(95) / math.cos(15)
print(resultado)

